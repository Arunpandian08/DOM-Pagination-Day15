const page = document.querySelector(".pageChanges");

function showPage(pageNumber, Data) {
  const startIndex = (pageNumber - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  const itemsToDisplay = Data.slice(startIndex, endIndex);
  createTable(itemsToDisplay);
  page.innerHTML = "Current Page: " + pageNumber;

}
 
function createTable(data) {
  const tableContainer = document.querySelector(".table-responsive");
  const table = document.createElement("table");
  table.className="table table-bordered";
  table.id="table";

  const head = document.createElement("thead")
  const headRow = document.createElement("tr");


  // Create table header
  Object.keys(data[0]).forEach((key) => {
    const th = document.createElement("th");
    th.textContent = key;
    headRow.appendChild(th);
  });

  head.appendChild(headRow)
  table.appendChild(head);

  const body = document.createElement("tbody")
  // Create table rows
  data.forEach((item) => {
    const row = document.createElement("tr");
    Object.values(item).forEach((value) => {
      const td = document.createElement("td");
      td.textContent = value;
      row.appendChild(td);
    });
    body.appendChild(row);
  });
table.appendChild(body);

  // Clear previous content and append the new table
  tableContainer.innerHTML = "";
  tableContainer.appendChild(table);
}

function updatePagination(totalPages, currentPage) {
  // Update the active class for pagination buttons
  for (let i = 1; i <= 10; i++) {
    const btn = document.querySelector(`#btn${i}`);
    if (i === currentPage) {
      btn.classList.add("active");
    } else {
      btn.classList.remove("active");
    }
  }
}
